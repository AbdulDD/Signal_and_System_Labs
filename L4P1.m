
i = input('input any number(must be within limit of 1 to 7)');  % input statement 
switch i                                                        % Switch case
    case 1
        disp('Monday')
    case 2 
        disp('Tuesday')
    case 3
        disp('Wednsday')
    case 4
        disp('Thursday')
    case 5
        disp('Friday')
    case 6
        disp('Saturday')
    case 7
        disp('Sunday')
    otherwise 
end                                                             % end of function